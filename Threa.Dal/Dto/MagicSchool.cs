namespace Threa.Dal.Dto;

/// <summary>
/// Represents the different schools of magic.
/// Each school has its own mana pool and set of spells.
/// </summary>
public enum MagicSchool
{
    /// <summary>
    /// Fire magic - offensive spells, heat, light through flame.
    /// </summary>
    Fire = 1,

    /// <summary>
    /// Water magic - ice, healing, purification, flow.
    /// </summary>
    Water = 2,

    /// <summary>
    /// Light magic - illumination, truth, protection, banishment.
    /// </summary>
    Light = 3,

    /// <summary>
    /// Life magic - healing, growth, vitality, nature.
    /// </summary>
    Life = 4
}
